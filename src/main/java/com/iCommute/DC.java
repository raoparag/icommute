package com.iCommute;

public class DC {
  
  private String dcName;
  private Integer dcID;
  private Integer dcLocID;
  public String getDcName() {
    return dcName;
  }
  public void setDcName(String dcName) {
    this.dcName = dcName;
  }
  public Integer getDcID() {
    return dcID;
  }
  public void setDcID(Integer dcID) {
    this.dcID = dcID;
  }
  public Integer getDcLocID() {
    return dcLocID;
  }
  public void setDcLocID(Integer dcLocID) {
    this.dcLocID = dcLocID;
  } 



}
